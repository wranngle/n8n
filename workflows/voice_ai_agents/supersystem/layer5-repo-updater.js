/**
 * Layer 5: Claude Code Auto-Commit Integration
 * 
 * Automatically commits learnings to the repository:
 * - CLAUDE.md updates
 * - Known bugs registry
 * - Skill updates
 * - Workflow templates
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const CONFIG = {
  REPO_ROOT: path.resolve(__dirname, '..', '..', '..', '..'),
  KNOWN_BUGS_DIR: 'context/known-bugs',
  SKILLS_DIR: '.claude/skills',
  CLAUDE_MD: 'CLAUDE.md',
  AUTO_COMMIT: process.env.AUTO_COMMIT !== 'false'  // Disable with AUTO_COMMIT=false
};

/**
 * Execute git command
 */
function git(command) {
  try {
    return execSync(`git ${command}`, { 
      cwd: CONFIG.REPO_ROOT,
      encoding: 'utf8',
      stdio: ['pipe', 'pipe', 'pipe']
    }).trim();
  } catch (error) {
    console.error(`[Layer5] Git error: ${error.message}`);
    return null;
  }
}

/**
 * Check if repo has uncommitted changes
 */
function hasUncommittedChanges() {
  const status = git('status --porcelain');
  return status && status.length > 0;
}

/**
 * Add known bug to registry
 */
function addKnownBug(bugData) {
  const { 
    id, 
    title, 
    description, 
    symptoms, 
    rootCause, 
    solution, 
    preventionPattern 
  } = bugData;

  const bugDir = path.join(CONFIG.REPO_ROOT, CONFIG.KNOWN_BUGS_DIR);
  if (!fs.existsSync(bugDir)) {
    fs.mkdirSync(bugDir, { recursive: true });
  }

  const filename = `${id || title.toLowerCase().replace(/\s+/g, '-')}.md`;
  const filepath = path.join(bugDir, filename);

  const content = `# ${title}

## Symptoms
${symptoms || description}

## Root Cause
${rootCause}

## Solution
${solution}

## Prevention Pattern
\`\`\`javascript
${preventionPattern || '// Add prevention code here'}
\`\`\`

---
*Auto-generated by Supersystem Layer 5*
*Created: ${new Date().toISOString()}*
`;

  fs.writeFileSync(filepath, content);
  
  if (CONFIG.AUTO_COMMIT) {
    git(`add "${filepath}"`);
  }

  return { success: true, filepath };
}

/**
 * Update skill file
 */
function updateSkill(skillName, updates) {
  const skillPath = path.join(CONFIG.REPO_ROOT, CONFIG.SKILLS_DIR, skillName, 'SKILL.md');
  
  if (!fs.existsSync(skillPath)) {
    console.error(`[Layer5] Skill not found: ${skillName}`);
    return { success: false, error: 'Skill not found' };
  }

  let content = fs.readFileSync(skillPath, 'utf8');

  // Append new patterns/knowledge
  if (updates.appendSection) {
    content += `\n\n## ${updates.appendSection.title}\n\n${updates.appendSection.content}`;
  }

  // Replace specific section
  if (updates.replaceSection) {
    const { marker, content: newContent } = updates.replaceSection;
    const regex = new RegExp(`(## ${marker}[\\s\\S]*?)(?=## |$)`, 'g');
    content = content.replace(regex, `## ${marker}\n\n${newContent}\n\n`);
  }

  fs.writeFileSync(skillPath, content);

  if (CONFIG.AUTO_COMMIT) {
    git(`add "${skillPath}"`);
  }

  return { success: true, skillPath };
}

/**
 * Append to CLAUDE.md
 */
function appendToClaudeMd(section, content) {
  const claudeMdPath = path.join(CONFIG.REPO_ROOT, CONFIG.CLAUDE_MD);
  
  if (!fs.existsSync(claudeMdPath)) {
    console.error(`[Layer5] CLAUDE.md not found`);
    return { success: false, error: 'CLAUDE.md not found' };
  }

  let claudeMd = fs.readFileSync(claudeMdPath, 'utf8');

  // Find section and append
  const sectionRegex = new RegExp(`(## ${section}[\\s\\S]*?)(?=## |---\\s*$|$)`, 'g');
  const match = claudeMd.match(sectionRegex);

  if (match) {
    // Append to existing section
    claudeMd = claudeMd.replace(sectionRegex, `$1\n${content}\n`);
  } else {
    // Add new section at end
    claudeMd += `\n\n## ${section}\n\n${content}\n`;
  }

  fs.writeFileSync(claudeMdPath, claudeMd);

  if (CONFIG.AUTO_COMMIT) {
    git(`add "${claudeMdPath}"`);
  }

  return { success: true, claudeMdPath };
}

/**
 * Save workflow template
 */
function saveWorkflowTemplate(templateName, workflowJson, category = 'voice_ai_agents') {
  const templatesDir = path.join(CONFIG.REPO_ROOT, 'workflows', category, 'templates');
  
  if (!fs.existsSync(templatesDir)) {
    fs.mkdirSync(templatesDir, { recursive: true });
  }

  const filepath = path.join(templatesDir, `${templateName}.json`);
  fs.writeFileSync(filepath, JSON.stringify(workflowJson, null, 2));

  if (CONFIG.AUTO_COMMIT) {
    git(`add "${filepath}"`);
  }

  return { success: true, filepath };
}

/**
 * Commit all staged changes
 */
function commitChanges(message, details = '') {
  if (!CONFIG.AUTO_COMMIT) {
    console.log('[Layer5] Auto-commit disabled, skipping commit');
    return { success: false, reason: 'Auto-commit disabled' };
  }

  if (!hasUncommittedChanges()) {
    return { success: false, reason: 'No changes to commit' };
  }

  const fullMessage = `[supersystem] ${message}

${details}

🤖 Generated by Supersystem Auto-Corrector
Co-Authored-By: Claude Code <noreply@anthropic.com>`;

  try {
    // Use heredoc-style commit for proper formatting
    const result = git(`commit -m "${fullMessage.replace(/"/g, '\\"')}"`);
    return { success: true, result };
  } catch (error) {
    return { success: false, error: error.message };
  }
}

/**
 * Main entry point for Layer 4 Gemini calls
 */
async function commitLearning(learningType, title, content, filePath = null) {
  let result;

  switch (learningType) {
    case 'known_bug':
      result = addKnownBug({
        title,
        description: content,
        rootCause: content,
        solution: content
      });
      break;

    case 'pattern':
      result = appendToClaudeMd('WORKFLOW PATTERNS', `### ${title}\n\n${content}`);
      break;

    case 'skill_update':
      // Extract skill name from title (e.g., "n8n-validation-expert: new rule")
      const [skillName, ...rest] = title.split(':');
      result = updateSkill(skillName.trim(), {
        appendSection: {
          title: rest.join(':').trim() || 'Auto-Generated Update',
          content
        }
      });
      break;

    case 'claude_md':
      result = appendToClaudeMd(title, content);
      break;

    default:
      return { success: false, error: `Unknown learning type: ${learningType}` };
  }

  // Auto-commit if successful
  if (result.success && CONFIG.AUTO_COMMIT) {
    const commitResult = commitChanges(
      `${learningType}: ${title}`,
      `Auto-generated learning from supersystem failure analysis.`
    );
    return { ...result, commit: commitResult };
  }

  return result;
}

/**
 * Generate improvement report
 */
function generateImprovementReport(remediations) {
  const reportPath = path.join(__dirname, 'tests', 'improvement-suggestions.md');
  
  let report = `# Improvement Suggestions

Generated: ${new Date().toISOString()}

---

`;

  for (const rem of remediations) {
    // BUG FIX: Map remediation structure to display fields
    const title = rem.pattern?.message || rem.functionCall?.name || rem.research?.query || 'Unknown';
    const type = rem.pattern?.type || rem.functionCall?.name || 'Remediation';
    const layer = rem.layer || (rem.functionCall ? 4 : 'Unknown');
    const success = rem.success ? 'SUCCESS' : 'FAILED';
    const description = rem.fix?.suggestion || 
                       rem.research?.recommendation?.action || 
                       rem.result?.message ||
                       (rem.error ? `Error: ${rem.error}` : '');

    report += `## [${success}] ${title}

**Type**: ${type}
**Layer**: ${layer}

${description}

`;

    // Add research recommendations if available
    if (rem.research?.recommendation) {
      report += `**Research Recommendation**: ${rem.research.recommendation.action}\n`;
      if (rem.research.recommendation.confidence) {
        report += `**Confidence**: ${rem.research.recommendation.confidence}\n`;
      }
      report += '\n';
    }

    // Add function call details if available
    if (rem.functionCall?.args) {
      report += `**Args**: \`${JSON.stringify(rem.functionCall.args)}\`\n\n`;
    }

    report += `---\n\n`;
  }

  fs.writeFileSync(reportPath, report);
  return { success: true, reportPath };
}

